package model;

/**
 * The Genre enumeration represents different genres for books or literary works.
 */
public enum Genre {

    /**
     * Represents the Fiction Science genre.
     */
    FICTION_SCIENCE,

    /**
     * Represents the Fantasy genre.
     */
    FANTASY,

    /**
     * Represents the Historical Novel genre.
     */
    HISTORICAL_NOVEL;
}
