package model;

/**
 * The Category enumeration represents the categories of products in the library.
 * It includes the following categories: VARIETIES, DESIGN, SCIENTIST.
 */
public enum Category {
    /**
     * Represents the VARIETIES category.
     */
    VARIETIES,
    
    /**
     * Represents the DESIGN category.
     */
    DESIGN,
    
    /**
     * Represents the SCIENTIST category.
     */
    SCIENTIST;
}
